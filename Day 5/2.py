# Tuple
fruit_tuple = ('apple', 'banana', 'cherry', 'date', 'elderberry')

# Accessing elements using positive and negative indexing...
first_element = fruit_tuple[0]
third_element = fruit_tuple[2]
last_element = fruit_tuple[-1]
second_last_element = fruit_tuple[-2]

print(f"First element: {first_element}")
print(f"Third element: {third_element}")
print(f"Last element: {last_element}")
print(f"Second last element: {second_last_element}")
