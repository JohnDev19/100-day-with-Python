# Integers
age = 25
year = 2024
hex_value = 0xFF  # Hexadecimal representation
binary_value = 0b1010  # Binary representation

# Floats
height = 5.9
pi = 3.14159
scientific_notation = 1.23e4  # 1.23 * 10^4

# Strings
name = "John Doe"
greeting = "Hello, World!"
multiline_string = """This is a
multiline string that spans
multiple lines."""

# Booleans
is_student = True
has_license = False

# Just a number
complex_number = 3 + 4j

print(f"Age: {age} years")
print(f"Year: {year}")
print(f"Hex value (in decimal): {hex_value}")
print(f"Binary value (in decimal): {binary_value}")

print(f"Height: {height} feet")
print(f"Value of Pi: {pi}")
print(f"Scientific notation: {scientific_notation}")

print(f"Name: {name}")
print(f"Greeting: {greeting}")
print("Multiline String:")
print(multiline_string)

print(f"Is student? {is_student}")
print(f"Has license? {has_license}")

print(f"Just a Number: {complex_number}")
print(f"Real part: {complex_number.real}, Imaginary part: {complex_number.imag}")
